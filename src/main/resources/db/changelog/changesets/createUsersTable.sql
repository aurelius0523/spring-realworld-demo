CREATE TABLE users
(
    id       INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email    varchar NOT NULL,
    username varchar NOT NULL,
    bio      varchar,
    image    varchar,
    password varchar,
    created_at timestamptz,
    modified_at timestamptz
);

CREATE UNIQUE INDEX users_username on users(username);

